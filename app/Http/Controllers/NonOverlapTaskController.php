     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $user = auth()->user();
        $start_date = now();
        $end_date = $start_date->copy()->addMonth(1); // 그대로 계산함수 쓰면 원본 인스턴스가 바뀌므로, copy()로 복사 후 계산.

        $tasks = $user->tasks() // hasManyThrough() 관계로 설정한 tasks() 메소드를 호출
            ->orderBy('due_date', 'desc')
            ->get();

        return view('task.index', compact('tasks','start_date','end_date'));
    }

